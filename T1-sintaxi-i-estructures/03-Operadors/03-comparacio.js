let a = 10;
let b = 5;
// Operadors relacionals
console.log('Relacionals');
console.log(a > b, a + ' > ' + b);
console.log(a >= b, a + ' >= ' + b);
console.log(a < b, a + ' < ' + b);
console.log(a <= b, a + ' <= ' + b);
// Operadors d'igualtat
console.log('Igualtat');
let c = 10; //number
let d = '10'; // string
console.log(a == c, a + ' == ' + c);
console.log(a != c, a + ' != ' + c);
console.log(a == d, a + ' == ' + d);
// Compara no només el valor sinó també el tipus
console.log(a === d, a + ' === ' + d);
console.log(a !== d, a + ' !== ' + d);
